package com.csis3275.Model_group3;

public class Carlos_group3 {
	
	String name;
	String about;
	
	
	public Carlos_group3() {
		//default constructor
	}
	
	public Carlos_group3(String name, String about) {
		super();
		this.name = name;
		this.about = about;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAbout() {
		return about;
	}
	public void setAbout(String about) {
		this.about = about;
	}
	
	

}
